package records;

public class LocalDate {
    int year;
    int month;
    int day;
    public int getYearValue() {
        return this.year;
    }

    public int getMonthValue() {
        return this.month;
    }

    public int getDayValue() {
        return this.day;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public void setDay(int day) {
        this.day = day;
    }
}
